top = ( '┌', '─',  '┐', '┬' )
mid = ( '│', '{}', '│'      )
bot = ( '└', '─',  '┘', '┴' )


def make_field(word: str):
    chars = len(word)
    top_field = f"{top[0]}{(top[1]+top[3]) * (chars-1)}{top[1]}{top[2]}\n"
    mid_field = f"{(mid[0]+mid[1]) * (chars)}{mid[2]}\n"
    bot_field = f"{bot[0]}{(bot[1]+bot[3]) * (chars-1)}{bot[1]}{bot[2]}\n"

    return (top_field+mid_field+bot_field).format(*list(word))
